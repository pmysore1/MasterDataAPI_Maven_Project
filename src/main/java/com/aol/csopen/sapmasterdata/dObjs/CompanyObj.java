public class CompanyObj extends BasicObj  {
    private String compCode;
    private String compDesc;
    private String country;
    private String sapDocType;
    private String sapPgroup;

    /**
     * @return the compCode
     */
    public String getCompCode() {
        return compCode;
    }

    /**
     * @param compCode the compCode to set
     */
    public void setCompCode(String compCode) {
        this.compCode = compCode;
    }

    /**
     * @return the compDesc
     */
    public String getCompDesc() {
        return compDesc;
    }

    /**
     * @param compDesc the compDesc to set
     */
    public void setCompDesc(String compDesc) {
        this.compDesc = compDesc;
    }

    /**
     * @return the country
     */
    public String getCountry() {
        return country;
    }

    /**
     * @param country the country to set
     */
    public void setCountry(String country) {
        this.country = country;
    }

    /**
     * @return the sapDocType
     */
    public String getSapDocType() {
        return sapDocType;
    }

    /**
     * @param sapDocType the sapDocType to set
     */
    public void setSapDocType(String sapDocType) {
        this.sapDocType = sapDocType;
    }

    /**
     * @return the sapPgroup
     */
    public String getSapPgroup() {
        return sapPgroup;
    }

    /**
     * @param sapPgroup the sapPgroup to set
     */
    public void setSapPgroup(String sapPgroup) {
        this.sapPgroup = sapPgroup;
    }
}

  
